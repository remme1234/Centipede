package egovframework.example.review.service;

// 사이즈 리뷰 select box에서 쓰일 VO
public class ReviewVO {

	private String catCd;
	private String brndNm;
	private String sortNo;
	private String regr;
	private String regDt;
	private String gdCd;
	private String gdNm;
	
	
	public String getCatCd() {
		return catCd;
	}
	public void setCatCd(String catCd) {
		this.catCd = catCd;
	}
	public String getBrndNm() {
		return brndNm;
	}
	public void setBrndNm(String brndNm) {
		this.brndNm = brndNm;
	}
	public String getSortNo() {
		return sortNo;
	}
	public void setSortNo(String sortNo) {
		this.sortNo = sortNo;
	}
	public String getRegr() {
		return regr;
	}
	public void setRegr(String regr) {
		this.regr = regr;
	}
	public String getRegDt() {
		return regDt;
	}
	public void setRegDt(String regDt) {
		this.regDt = regDt;
	}
	public String getGdCd() {
		return gdCd;
	}
	public void setGdCd(String gdCd) {
		this.gdCd = gdCd;
	}
	public String getGdNm() {
		return gdNm;
	}
	public void setGdNm(String gdNm) {
		this.gdNm = gdNm;
	}
	
}
